public class PrintOddNumbers {
    public static void main(String[] args) {
        for (int i = 1; i <= 30; i += 2) {
            System.out.print(i + " ");
        }
    }
}
/*Explanation:
Uses a for loop starting at 1 and incrementing by 2 each time.
Prints numbers: 1, 3, 5, ..., 29.*/

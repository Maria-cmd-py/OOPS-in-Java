public class CountdownTimer {
    public static void main(String[] args) {
        int i = 20;
        while (i >= 1) {
            System.out.print(i + " ");
            i--;
        }
    }
}
/*Explanation:
Starts at 20 and uses a while loop to count down.
Prints numbers: 20, 19, 18, ..., 1.*/